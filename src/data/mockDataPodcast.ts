  // Helper function to get user stake for a podcast
  export const getUserStake = (podcastId: string) => {
    const podcast = mockPodcasts.find(p => p.id === podcastId);
    if (!podcast) return 0;
    
    // In a real app, this would be based on the user's actual stake
    // For demo purposes, we'll return 10% of the total staked amount
    return podcast.stakedAmount * 0.1;
  };
  
  // Helper function to stake tokens in a podcast
  export const stakeTokens = (podcastId: string, amount: number) => {
    const podcast = mockPodcasts.find(p => p.id === podcastId);
    if (!podcast) return false;
    
    // Update podcast state (in a real app, this would be a backend call)
    podcast.stakedAmount += amount;
    podcast.tokenPrice += amount * 0.0001; // Simple price impact for demo
    podcast.roi += 0.5; // Simple ROI change for demo
    
    return true;
  };
  
  // Helper function to withdraw tokens from a podcast
  export const withdrawTokens = (podcastId: string, amount: number) => {
    const podcast = mockPodcasts.find(p => p.id === podcastId);
    if (!podcast) return false;
    
    // Update podcast state (in a real app, this would be a backend call)
    podcast.stakedAmount -= amount;
    podcast.tokenPrice -= amount * 0.0001; // Simple price impact for demo
    podcast.roi -= 0.5; // Simple ROI change for demo
    
    return true;
  };
  